/**
 The `BubbleColor` enum provides an enumeration of all possible 
 colors of a bubble. 
 
 It has a String raw value that is used for encoding and decoding.
 */
enum BubbleColor: String {
    case Blue
    case Red
    case Orange
    case Green
}
